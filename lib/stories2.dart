class StoryPage extends StatelessWidget {
  const StoryPage({Key? key}) : super(key: key);

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(title: Text('Stories')),
      body: Container(
          child: ListView.builder(
        itemCount: storyList.length,
        itemBuilder: (BuildContext context, int index) {
          return Card(
            elevation: 4.5,
            color: Colors.white,
            child: ListTile(
              // title: Text(this.wordsinVocabClass[index].WordEs),
              // subtitle:
              title: Text(storyList[index].storyNameES),
              subtitle: Text(storyList[index].storyNameEng),
              onTap: () {
                Navigator.push(
                    context,
                    MaterialPageRoute(
                        builder: (context) =>
                            storypart(storyVar: storyList[index])));
              },
            ),
          );
        },
      )),
    );
  }
}

class storypart extends StatelessWidget {
  final story storyVar;
  const storypart({
    Key? key,
    required this.storyVar,
  }) : super(key: key);

  @override
  Widget build(BuildContext context) {
    return Scaffold(
        appBar: AppBar(
            title: Text('${storyVar.storyNameES} : ${storyVar.storyNameEng}')),
        body: Container(
          child: ListView.builder(
              itemCount: storyVar.storyText.length,
              itemBuilder: (BuildContext context, int index) {
                return Text(
                  storyVar.storyText[index],
                  style: TextStyle(fontSize: 30),
                );
              }),
        ));
  }
}
